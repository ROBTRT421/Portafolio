print ('   **SUMA O ADICION**  ')
print ("""La suma o adicion, es la reunion de dos o mas conjuntos llamados 'sumados' en un solo conjunto llamado
'suma o total'. su signo en Python es (+) y se coloca entre los sumandos \n""") 
N1 = 5
N2 = 4
R = N1 + N2
print ('El Resultado de la suma es: ' + str (R))

print ('\n  **RESTA O SUSTRACCION**  ')
print ("""La resta, es la operacion contaria a la suma, y tambien recibe el nombre de sustraccion. la cual 
consiste en extraer o quitar de un numero mayor otro menor. Su signo en Python es (-). \n""")
N1 = 5
N2 = 4
R = N1 - N2 
print ('El Resultado de la Resta es: ' + str (R))

print ('\n  **MULTIPLICACION**   ')
print ("""La Multiplicacion, es la operacion matematica que consiste en halla el resultado de sumar un numero
tantas veces como indique otro. Su signo en Python es (*) \n""")
N1 = 5
N2 = 4
R = N1 * N2
print ('El Resultado de la multiplicacion es: ' + str (R))

print ('\n  **EXPONENTE O POTENCIA**  ')
print ("""La potenciacion, es la operacion matematica mediante la cual multiplicamos un numero por si mismo las veces
que nos indique el exponente. Su representacioon en Python es (**). \n""")
N1 = 2
Exponente = 5
R = N1 ** Exponente
print ('El Resultado del exponente es: ' + str (R))

print ('\n  **DIVISION**   ')
print ("""La Division, es una operacion matematica que consiste en averiguar cuantas veces un numero esta contenido 
en otro numero. Su signo en Python es (/) \n""")
N1 = 4
N2 = 2
R = N1 / N2
print ('El Resultado de la Division es: ' + str(R))

print ('\n  **MODULO O RESTO**   ')
print ("""El modulo o resto, es la cantidad que sobra despues de efectuar una division, es decir, es el 
valor que se obtiene cuando un numero no puede ser dividido exactamente por otro. Su signo en Python es (%) \n""")
N1 = 30
N2 = 8
R = N1 % N2 
print ('El Resultado del Modulo es: ' + str (R))

print ('\n  **DIVISION ENTERA**   ')
print ("""La division entera, es una operacion que consiste en obtener el valor entero de un numero, el cual resulte
de una divicion entre dos numero decimales o reales. Su representacion en Python es (//). \n""")
N1 = 4
N2 = 2
R = N1 // N2
print ('El Resultado de la Division es: ' + str (R))