print ('                   ***OPERADORES DE ASIGNACION***                   ')
print ("""Un operador de Asignacion, como su nombre lo indica, se encarga de asignar un valor  a la variable
de la izquierda basado en el valor de la derecha. El operador de asignacion mas utilizado es el igual (=), el 
cual asigna el valor que se encuentra de lado derecho a la variable de lado izquierdo. Es decir, X = Y asigna
el valor de Y a X. En Python, contamos con los siguientes operadores de asignacion : \n""" )

print ('-Operador de Asignacion.')
print ("""El operador de asignacion se implemenmta con ayuuda del signo igual a (=), es el mas simple de todos
y asigna a la variable de lado izquierdo cualquier variable o valor de lado derecho. """)
print (' X = 5 , donde asignamos el valor de 5 a la variable X.\n')

print ('-Operador de Asignacion de suma o adicion.')
print ("""El operador de asignacion de suma o adicion se implementa con la combinacion de los signos (+=), asigna en forma de suma a la variable de lado izquierdo, el valor del lado derech.""")
print ("""Si la variable ya tiene un valor asignado desde una instruccion anterior, con el operador (+=), en lugar de sustituirlo se suma.""")
print ('X = 3')
print ('X += 5')
print ('X = 8 \n')

print ('-Operador de Asignacion de Resta.')
print ("""El operador de asignacion de resta se implementa con la combinacion de los signos (-=), asigna en forma de resta a la variuable de lado izquierdo, el valor de lado derecho.""")
print ("""Al igual que el operador de suma o adicion, este operador restara a la variable sobre el valor antes asignado.""")
print ('X = 8') 
print ('X -= 5') 
print ('X = 3 \n')

print ('-Operador de Asignacion de Multiplicacion')
print ("""El operador de asignacion de multiplicacion se implementa con la combinacion de los signos (*=), asigna en forma de multiplicacion a la varibale de lado izquiuerdo, el valor de lado derecho.""")
print ("""Al igual que los dos anteriores, este operador multiplica a la variable sobre el valor antes asignado.""")
print ('X = 3') 
print ('X *= 5') 
print ('X = 15. \n')

print ('-Operador de Asignacion de Division.')
print ("""El operador de asignacion de division se implementa con la combinacion de los signos (/=), asigna en forma de division a la variable de lado izquierdo, el valor de lado derecho.""")
print ("""Este operador divide la variable, sobre el valor antes asignado.""")
print ('X = 10')
print ('X /= 3')
print ('X = 3.3. \n')

print ('-Operador de Asignacion de Division Entera.')
print ("""El operador de asignacion de division entera se implementa con la combinacion de los signos (//=), calcula  y asigna la division entera a la variable de lado izquiero, el valor de lado derecho. """)
print ("""Este operador tiene la misma funcion que el operador de division con la unica caracteristica de que no te otorgara decimales y optara por dar unicamente resultados enteros. """ )
print ('X = 10')
print ('X //= 3') 
print ('X = 3. \n')

print ('-Operador de Asignacion de Exponente.')
print ("""El operador de asignacion de exponente se implementa con la combinacion de los signos (**=), calcula y asigna el exponente a la variable de lado izquiero, el valor de lado derecho. """)
print ("""Este operador tiene la funcion de asignar el valor exponencial a la variable designada la cual ya cuenta con un valor definido. """)
print ('X = 2') 
print ('X **= 5 o lo que es tambien 2 x 2 x 2 x 2 x 2. ')
print ('X = 32. \n')

print ('-Operador de Asignacion de Modulo o Resto.')
print ("""El operador de asignacion de modulo o resto se implementa con la combinacion de los signos (%=), devuelve y asigna el resto o modulo de la division a la variable de lado izquiero con el valor de lado derecho. """)
print ("""Este operador tiene la funcion de asignarle el valor del modulo o resto que se obtendra entre la division del valor asignado con anterioridad de la variable y el numero que nosotros asignaremos. """)
print ('X = 10')
print ('X %= 2') 
print ('X = 0, no olvides que el residuo de una division, es el "sobrante" de la misma. \n')